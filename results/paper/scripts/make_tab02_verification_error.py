#!/usr/bin/env python3
from __future__ import annotations

from pathlib import Path
import pandas as pd

SRC = Path("results/paper/data/tables/tab02_verification_error/source/tab02_verification_error.csv")
OUT_TEX = Path("docs/paper/latex/tables/qflow_tab02_verification_error_main.tex")
OUT_CSV = Path("results/paper/data/tables/tab02_verification_error/tab02_verification_error_export.csv")

def esc(s: str) -> str:
    return (
        str(s)
        .replace("%", r"\%")
        .replace("<=", r"$\leq$")
        .replace("1.22e-4", r"$1.22\times 10^{-4}$")
    )

def main():
    if not SRC.exists():
        raise FileNotFoundError(f"Missing source CSV: {SRC}")

    df = pd.read_csv(SRC)
    OUT_CSV.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(OUT_CSV, index=False)

    lines = []
    lines.append(r"% Auto-generated by make_tab02_verification_error.py")
    lines.append(r"% Requires \usepackage{booktabs,array,tabularx}")
    lines.append(r"\begin{tabularx}{\textwidth}{>{\raggedright\arraybackslash}p{2.8cm} >{\raggedright\arraybackslash}X >{\raggedright\arraybackslash}p{2.2cm} >{\raggedright\arraybackslash}p{2.0cm} >{\raggedright\arraybackslash}p{1.4cm}}")
    lines.append(r"\toprule")
    lines.append(r"Check & Quantity checked & Metric & Result & Status \\")
    lines.append(r"\midrule")

    for _, r in df.iterrows():
        lines.append(
            f"{esc(r['check_item'])} & "
            f"{esc(r['quantity_checked'])} & "
            f"{esc(r['metric'])} & "
            f"{esc(r['result'])} & "
            f"{esc(r['status'])} \\\\"
        )

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabularx}")

    OUT_TEX.parent.mkdir(parents=True, exist_ok=True)
    OUT_TEX.write_text("\n".join(lines), encoding="utf-8")

    print(f"Wrote {OUT_TEX}")
    print(f"Wrote {OUT_CSV}")

if __name__ == "__main__":
    main()
